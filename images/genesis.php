       <h2 align="center" class="title">Genesis</h2>
       
   <p align="justify">        &nbsp;&nbsp;&nbsp;&nbsp;<B>O</B>ne fine day while discussing with my father about computers,i was very surprised by the dynamics of change that is continuing to surround me.Not going into the details and getting to the point,we are living in a world of constant change(pun intended)."iMinds" is a step taken towards preparing for the world that awaits us.We all are unique,like everyone else (...and here i go      again,sorry).Regular lectures did really bore me,specially when it includes the same old routine and more emphasis on remembering things and scoring marks in the exam than actually understanding the concept and meaning.In search of something more interesting than normal,i ventured in to the vast open space exploring different possibilities and directions to work.It was after two years of entering college did I start off with iMinds,a group which allows to explore newer ideas and work towards the all round development.We are not technical experts though,but we try and look into the finer details.As it is said,God is in small things.This group is nothing more than a bunch of people who try to learn and emphasize on the basics.We like sharing our knowledge with the world around us.Sharing is a way to open up newer horizons to work upon,it builds bonds between people.A lot can be achieved through sharing.</p>

    <p align="justify">     While sitting in the library with a friend,we suddenly started off with the topic of designing a website for friends and students to access some knowledge and programs we made.Then started the work to build such a site which now you are reading from.It was a side project which the members of the group started.This website is developed keeping in mind the students perspective.It contains programs and other articles which are useful to bring about some confidence and give a direction to the chain of thoughts.This website also provides a platform for people to share their knowledge to the world and reaching out to people who may require some assistance and guidance.</p><p align="justify">

        To explore the plethora of possibilities,this website is just a small step.Though this site now is static,in some time,this would be converted into a dynamic site where the users would be able to log in and share their knowledge and submit articles,scripts and programs.Hope you find this site resourceful and helpful to take< a step forward and join us in our efforts.
