              <br>  With Windows 8 already taking UI direction from Windows Phone, and Windows Phone taking the kernel from Windows 8, this essentially means that Windows Phone 8 and Windows 8 will be the same OS. By basing both operating systems on the same kernel, developers at Microsoft can share code for common features between the OS. This would include code for sensors, networking, multimedia, etc. So what does this mean for Windows Phone 7 apps, considering that Windows Phone 8 changes the architecture of the OS. Well not much. Windows Phone 7 applications are not built on native code, so it is easy for them to be supported on Windows Phone 8. On the other hand, this might make it possible for developers to share more code between Windows 8 apps and Windows Phone 8 apps. <br>
