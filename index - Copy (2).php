        			<font size="+3"><center>Features Of JAVA?</center></font>
                    <ul id="MenuBar2" class="MenuBarVertical">
                    <li><a class="MenuBarItemSubmenu style12">1) Simple</a>
          <ul>
            <li><a>Java is a small and simple language. Many features of C and C + + that are either redundant or sources of unreliable code are not part of Java. For example, Java does not use pointers, preprocessor  header files, goto statement and many others. Java is a simplified version of C++.  Java is simple for development.</a></li><br>
            
          </ul>
        <li><a  class="MenuBarItemSubmenu style12">2) Platform Independent</a>
          <ul>
            <li><a >Platform independence means that a program can run on any computer system. Java programs can run on any system for which a Java virtual machine has been installed.
Platform independence at the source level means that you can move Java source files from system to system and have them compile and run cleanly on any system. 
Platform independence in Java doesn't stop at the source level, however. Java compiled binary files are also platform independent and can run on multiple platforms (if they have a Java virtual machine available) without the need to recompile the source. 
</a></li><br>
          </ul>
        <li><a class="MenuBarItemSubmenu style12">3) Portable</a>
          <ul>
            <li><a>The most significant and important difference between java and other languages is its portability.
Java programs can be easily moved from one computer system to other.
Changes and upgrades in OS, processor and system resources will not force any change in java program.
Java ensures portability in two ways
   ->First, java compiler generates byte code instructions that can be implemented on any machine.
   ->Secondly, the size of primitive data types is machine independent.
</a></li><br>
          </ul>
        <li><a class="MenuBarItemSubmenu style12">4) Compiled And Interpreted</a>
          <ul>
            <li><a>Usually a computer language is either compiled or interpreted. Java combines both these approaches thus making Java a two-stage system. First, Java compiler translates source code into what is known as bytecode instructions. Bytecodes are not machine instructions and therefore, in the second stage, Java interpreter generates machine code that can be directly executed by the machine that is running the Java program. We can thus say that Java is both a compiled and an interpreted language.</a></li><br>
          </ul>
          <li><a  class="MenuBarItemSubmenu style12">5) Distributed</a>
          <ul>
            <li><a>Java is designed as a distributed language for creating applications on networks.  It has the ability to share both data and programs. Java applications can open and access remote objects on Internet as easily as they can do in a local system. This enables multiple programmers at multiple remote locations to collaborate and work together on a single project.</a></li><br>
          </ul>
          <li><a class="MenuBarItemSubmenu style12">6) Multithreaded</a>
          <ul>
            <li><a >Multithreaded means handling multiple tasks simultaneously. Java supports multithreaded programs. This means that we need not wait for the application to finish one task before beginning another. For example, we can listen to an audio clip while scrolling a page and at the same time download an applet from a distant computer. This feature greatly improves the interactive performance of graphical applications.
         The Java runtime comes with tools that support multiprocess synchronization and construct smoothly running interactive systems
</a></li><br>
          </ul>
          <li><a  class="MenuBarItemSubmenu style12">7) High Performance</a>
          <ul>
            <li><a>Java perfoemance is impressive for an interpreted language mainly due to the use of intermediate bytecode. According to Sun, Java speed is comparable to the native C/C + + , Java architecture is also designed to reduce overheads during runtime. Further, the incorporation of multithreading enhances the overall execution speed of Java programs.</a></li><br>
          </ul>
          <li><a  class="MenuBarItemSubmenu style12">8) Dynamic</a>
          <ul>
            <li><a>Java is a dynamic language. Java is capable of dynamically linking in new class libraries, methods, and objects. Java can also determine the type of class through a query, making it possible to either dynamically link or abort the program, depending on the response.
        Java programs support functions written in other languages such as C and C + +. These functions are known as native methods. This facility enables the programmers to use the efficient functions available in this language. Native methods are linked dynamically at runtime.
</a></li><br>
          </ul>
          <li><a class="MenuBarItemSubmenu style12">9) Object Oriented</a>
          <ul>
            <li><a >Java is a true object-oriented language. Almost everything in Java is an object. All program code and data reside within objects and classes. Java comes with an extensive set of classes, arranged in packages, that we can use in our programs by inheritance. The object model  in Java is simple and easy to extend</a></li><br>
          </ul>
          <li><a class="MenuBarItemSubmenu style12">10) Robust And Secure</a>
          <ul>
            <li><a>Java is a robust language. It provides many safeguards to ensure reliable code. It has strict compile time and run time checking for data types. It is designed as a garbage-collected language relieving the programmers virtually all memory management problems. The absence of pointers in java ensures that programs cannot gain access to memory locations without proper authorization.</a></li><br>
          </ul>
