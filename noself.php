              <p class=d"latestnews"><center><img src="no_self_confidence.jpg"  length="135" width="160"/></center><br>&nbsp;&nbsp;There is no such thing called Self-Confidence
I was passing by my school when I saw a very flashy notice put up on the gate. I 
stopped my bike and turned back to read it carefully. The Notice was to make 
parents aware of a psychoanalysis programme conducted by the “competent 
counseling cell” (pun intended) for children from class 1 to class 7. I stood there 
reading the details of what was in store for this programme, in which I could find 
everything from anger management to multiple intelligence test, from social 
compatibility to self confidence. Now, it was this self-confidence thingy that 
prompted me to write this. I still recollect the school days when we were told about 
self confidence as a key to overcoming difficult situations. As kids we were afraid 
of speaking in public on the stage, it was then this “self-confidence” lecture came 
down upon us again. Our school library being one the best libraries in the state had 
a complete section on self help books, most of which were somewhere directly or 
indirectly linked to self-confidence, be it building up self-confidence or importance 
of self-confidence etc. At about every phase of life, there was a mention of this 
word by some person or the other. Pondering over this, I did find out really 
interesting things about Self-Confidence.<br>
<br>Now taking into account the general knowledge about self-confidence, it is having 
belief in one’s ability and talents to be used efficiently to accomplish a task. In 
very simple terms, it is having confidence in ‘self’. When I googled this word, I 
found the first page entries comprising of web-pages showing methods to shape 
self confidence, relating self-confidence to success and others reviving or building 
self-confidence. The actual meaning of self-confidence still remained obscure. I 
never saw self confidence with a perception generally accepted or viewed. Reading 
those self-help books which emphasize on self-confidence, attending personality 
development classes which tried to put to test our self confidence made me think 
more and more about it. Whenever any of my friend refuses to answer a question 
asked by a teacher in front of the whole class, the most common reply 
I get is “self confidence nahi hai”. Sitting at pandit, I do listening to people talking big stuff, 
building castles in the air. But when asked to the point about their unwillingness or 
their inaction, all I get is either stony silence or something somewhere linked to 
self confidence. Self-confidence books have become money minting areas for 
people around the world, the very essence of it has become a craze.<br>
<br>Coming to my perception, I don’t believe there is anything called self-confidence. 
It is all an illusion, or maybe putting it in less controversial terms, is a wrong 
heading to the actual book. There are hidden obscure things which I believe matter 
more. I believe it is the 'fear' that aptly fits in. For me, fear is what drives the whole 
scenario. The proper context of self-confidence is defined by<br>
1.  People with whom we interact<br>
2.  Our subconscious mind<br>
  
<br>If we take the context of interacting with people, it is not wrong to state that we 
tend not to speak freely or do certain things or accomplish certain goals because of 
constantly being under the fear of being judged and commented upon. The worst 
fear within us is that of being left alone. We all crave for people to hear us and 
understand us. Past memories and incidences hinder the actions or activities being 
pursued. There are times when you are made the object of constant criticism or 
verbal intimidation. Such incidences and memories instill in us fear. This fear then 
becomes the prime factor in incompetency even though there is potential and the 
fire.<br>
<br>I do interact with people who have undergone such things in school during their 
childhood. The fear within them is so overpowering, it gains control over their 
thinking process so effectively that it is very difficult to open their minds to newer 
horizons. If each one of us make sure that people around us are treated with respect 
and poking fun and verbal intimidation is practiced with discretion, there would be 
a very positive change, visible enough to do wonders. In simpler cases, it is the 
fear of failure, so people do not take chances. In any case, we succumb to fear. The 
rest of the self-confidence thingy is a whole load of old bollocks.<br>
<br>The context of our subconscious is very deep, vast and largely unexplored. It is 
best not to write about it for the time being. However, I realize whatever I have 
written above is highly controversial and that its acceptance would be based on 
individualistic perception. I again would highlight the importance of an underlying 
principle of group dynamics that greatly affects the overall scenario. There is a 
limit to making fun of someone within the group. You might be pampering your 
ego and boosting it. But in the end, the only thing that predominantly floats is fear. 
Everyone is different. We are unique like everyone else. A perception shift greatly 
improve things. We need to support each other.<br>
<br>As Dr. Nash said, the best result comes when you do what is best for yourself and 
the group. The aspect of Self Confidence is just a frosted glass. We need to see 
behind it, what lays hidden in plain sight is fear.<br>
<br>Quinn.. </p>
              
