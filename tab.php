&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;HRD ministry to involve 3 more IITs in Aakash tablet project<br>
 




<br>&nbsp;&nbsp;The HRD ministry is now planning to engage three more IITs in its Aakash tablet PC project &nbsp;&nbsp;in a bid to make the device more indigenous and reduce its price. Along with IIT-Rajasthan, which is leading the project, IIT-Mumbai, IIT-Kanpur and IIT-Kanpur are likely to be involved in the project. According to reports, senior officials from the HRD ministry and Department of IT met yesterday to discuss the matter. The officials stressed on making the device more Indian, as most of the components in the existing Aakash tablet are sourced from abroad. The plan is to make the device at least 90 per cent Indian by 2014. A special emphasis will be made on lowering the price of the Aakash tablet. Currently, the government is providing the Aakash tablet at a subsidised price of Rs. 1,100.<br>

<br>The development follows reports the government is now looking to public sector enterprise for supply of the Aakash tablet. The Aakash tablet is presently seeing a rough phase after it saw a massive response upon its launch. The spat between IIT-Rajasthan and DataWind has already delayed the supply of the Aakash tablet. According to reports, the government is now taking away responsibility of the procurement of the Aakash tablets from IIT-Rajasthan and DataWind was given extension only till March.<br>

<br>The Aakash tablet, also touted as the world's cheapest tablet, is an important project for the government. The ultra low-cost device has already created buzz worldwide with a number of countries showing interest in the device. The government will require 22 crore Aakash tablets in near future to achieve its mission of bridging the digital divide in the country. The HRD ministry has already drawn ire from many quarters over the poor performance and specifications of the Aakash tablet. The government now plans to come out with an improved Aakash tablet by April this year. <br>

